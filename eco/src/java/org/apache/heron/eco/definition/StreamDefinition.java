package org.apache.heron.eco.definition;

public class StreamDefinition {

  private String id;
  private String name;
  private String to;
  private String from;
  private GroupingDefinition grouping;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getTo() {
    return to;
  }

  public void setTo(String to) {
    this.to = to;
  }

  public String getFrom() {
    return from;
  }

  public void setFrom(String from) {
    this.from = from;
  }

  public GroupingDefinition getGrouping() {
    return grouping;
  }

  public void setGrouping(GroupingDefinition groupingDefinition) {
    this.grouping = groupingDefinition;
  }
}
